import Foundation
import UIKit

struct ThemeManager {
  static func primaryColor() -> UIColor {
    return UIColor(red: 58/255.0, green: 174/255.0, blue: 228/255.0, alpha: 1.0)
  }
  
  static func secondaryColor() -> UIColor {
    return UIColor(red: 241/255.0, green: 94/255.0, blue: 34/255.0, alpha: 1.0)
  }
}

